import gql from 'graphql-tag';

export const QUERY_THOUGHTS = gql`
  query projects($username: String) {
    projects(username: $username) {
      _id
      projectText
      createdAt
      username
      reactionCount
      reactions {
        _id
        createdAt
        username
        reactionBody
      }
    }
  }
`;

export const QUERY_THOUGHT = gql`
  query project($id: ID!) {
    project(_id: $id) {
      _id
      projectText
      createdAt
      username
      reactionCount
      reactions {
        _id
        createdAt
        username
        reactionBody
      }
    }
  }
`;

export const QUERY_USER = gql`
  query user($username: String!) {
    user(username: $username) {
      _id
      username
      email
      friendCount
      friends {
        _id
        username
      }
      projects {
        _id
        projectText
        createdAt
        reactionCount
      }
    }
  }
`;

